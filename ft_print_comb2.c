#include <unistd.h>

void    ft_print_comb2(void)
{
    int i = 0;
    while(i <= 98)
    {
        int j = i + 1;
        while(j <= 99)
        {
            if (i == 98 && j == 99)
            {
                char    c[5];
                c[0] = i / 10 + '0';
                c[1] = i % 10 + '0';
                c[2] = ' ';
                c[3] = j / 10 + '0';
                c[4] = j % 10 + '0';
                write(1, &c, 5);
            }
            else
            {
                char    c[6];
                c[0] = i / 10 + '0';
                c[1] = i % 10 + '0';
                c[2] = ' ';
                c[3] = j / 10 + '0';
                c[4] = j % 10 + '0';
                c[5] = ',';
                write(1, &c, 6);
            }
            j++;
        }
        i++;
    }
}

int     main()
{
    ft_print_comb2();
    return 0;
} 