#include <stdio.h>
#include "ft_printf.h"

int	main(void)
{
	int	real_len;
	int	my_len;

	// -------------------------------------------------------------
	// パターン1：超基本！文字と文字列（%c, %s, %%）
	// -------------------------------------------------------------
	printf("=== パターン1: 基本の文字・文字列 ===\n");
	
	real_len = printf("本家: %c %s %%\n", 'A', "Hello, 42!");
	my_len   = ft_printf("自作: %c %s %%\n", 'A', "Hello, 42!");
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	// -------------------------------------------------------------
	// パターン2：罠がいっぱい！10進数と最小値（%d, %i）
	// -------------------------------------------------------------
	printf("=== パターン2: 10進数（INT_MINの罠） ===\n");
	
	real_len = printf("本家: %d %i %d\n", 0, 12345, (int)-2147483648);
	my_len   = ft_printf("自作: %d %i %d\n", 0, 12345, (int)-2147483648);
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	// -------------------------------------------------------------
	// パターン3：マイナスがない世界！符号なし10進数（%u）
	// -------------------------------------------------------------
	printf("=== パターン3: 符号なし10進数 ===\n");
	
	// ※-1 を unsigned に渡すと、型変換で4294967295という最大値になります。これも本家と一致するかテスト！
	real_len = printf("本家: %u %u\n", 4294967295U, -1);
	my_len   = ft_printf("自作: %u %u\n", 4294967295U, -1);
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	// -------------------------------------------------------------
	// パターン4：16進数の小文字と大文字（%x, %X）
	// -------------------------------------------------------------
	printf("=== パターン4: 16進数（小文字・大文字） ===\n");
	
	real_len = printf("本家: %x %X %x\n", 0, 255, 3735928559U); // 3735928559は16進数で deadbeef
	my_len   = ft_printf("自作: %x %X %x\n", 0, 255, 3735928559U);
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	// -------------------------------------------------------------
	// パターン5：最難関！ポインタのアドレス（%p）
	// -------------------------------------------------------------
	printf("=== パターン5: ポインタアドレス ===\n");
	
	int	num = 42;
	real_len = printf("本家: %p %p\n", &num, NULL);
	my_len   = ft_printf("自作: %p %p\n", &num, NULL);
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	// -------------------------------------------------------------
	// パターン6：マフサヴィ（42のテストツール）でよく落ちる意地悪な文字列NULL
	// -------------------------------------------------------------
	printf("=== パターン6: 【最強の罠】文字列にNULLが来たら？ ===\n");
	
	char *empty_str = NULL;
	real_len = printf("本家: %s\n", empty_str);
	my_len   = ft_printf("自作: %s\n", empty_str);
	printf("戻り値 -> 本家: %d | 自作: %d\n\n", real_len, my_len);

	return (0);
}