#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int main(void)
{
	char	a[] = "Hello";
	int result = ft_strlen(a);
	printf("%d", result);
	return (0);
}