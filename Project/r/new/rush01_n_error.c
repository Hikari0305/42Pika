int	*error_check(int *vals, char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[j] != '\0')
	{
		if (s[j] >= '1' && s[j] <= '4')
		{
			if (i >= 16)
				return (0);
			vals[i++] = s[j] - '0';
		}
		else if (s[j] != ' ')
			return (0);
		j++;
	}
	if (i != 16)
		return (0);
	return (vals);
}
