#include "rush02.h"

int	ft_is_space(char c)
{
	return (c == ' ' || c == '\t' || c == '\n'
		|| c == '\r' || c == '\v' || c == '\f');
}

int	ft_is_digit(char c)
{
	return (c >= '0' && c <= '9');
}
