int	checker(int g[4][4], int row, int col)
{
	int	r;
	int	i;

	i = 0;
	r = 1;
	while (i != 4)
	{
		if ((r != g[(row + i) % 4][col])
		&& (r != g[row][(col + i) % 4])
		&& (r > g[row][col]))
		{
			i++;
		}
		else
		{
			r++;
			i = 0;
		}
		if (r == 5)
		{
			return (0);
		}
	}
	return (r);
}
