#include <unistd.h>

void    ft_print_comb (void)
{
    int i = 0;
    while (i <= 7)
    {
        int j = i + 1;
        while (j <= 8)
        {
            int k = j + 1;
            while (k <= 9)
            {
                if(i == 7 && j == 8 && k == 9)
                {
                    char    c[3];
                    c[0] = i + '0';
                    c[1] = j + '0';
                    c[2] = k + '0';
                    write(1, &c, 3);
                }
                else
                {
                    char    c[4];
                    c[0] = i + '0';
                    c[1] = j + '0';
                    c[2] = k + '0';
                    c[3] = ',';
                    write(1, &c, 4);
                }
                k++;
            }
            j++;
        }
        i++;
    }
    
}

int    main()
{
    ft_print_comb();
    return 0;
}