#include <unistd.h>

void	ft_ft (int *nbr)
{
	*nbr = 42;
}

int main (void)
{
	int x = 5;
	int *nbr = &x;
	ft_ft(nbr);

	char c;
	char d;

	c = x / 10 + '0';
	d = x % 10 + '0';
	write(1, &c, 1);
	write(1, &d, 1);
	
	return (0);
}