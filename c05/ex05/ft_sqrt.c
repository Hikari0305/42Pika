#include <stdio.h>

int	ft_sqrt(int nb)
{
    int	i;

    i = 0;
    while (i * i <= nb && i <= 46340)
    {
        i++;
        if (i * i == nb)
            return (i);
    }
		return (0);
}

int	main(void)
{
	printf("%d", ft_sqrt(2147395600));
	return (0);
}